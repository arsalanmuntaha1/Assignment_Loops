//------------------------------------------> ASSIGNMENT OF LOOP <-----------------------------------------------------------------------------

//------------------------------------------> COUNTER INCREMENT <------------------------------------------------------------------------------

let counter = 0; //initialize the counter
let step = 10;  //Define the step value
do {
    console.log(counter); //Print the current counter value
     counter+=step;      //Increment the counter by step
} while (counter <= 100);  //Loop continues until the counter value is less than 100
